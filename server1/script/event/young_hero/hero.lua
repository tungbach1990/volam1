function hero_get_task_account()
	local tb_tasklevel = {
		{1,		1},
		{20,	3},
		{50,	6},
		{80,	11},
		{100,	16},
		{201,	21},
		}
	local n_hardbase = 1;
	local n_hardtime = 0;
	local n_nexthard = 1;
	local ring	= GetByte(GetTask(1020),3);
	local hard	= GetByte(GetTask(1020),2);
	local times	= GetByte(GetTask(1020),1);
	local chain	= GetTask(1035);
	if (hard > 20) then
		hard = 20
	end
	
	for i=1, getn(tb_tasklevel) do
		if (hard >= tb_tasklevel[i][2]) then
			n_hardbase = tb_tasklevel[i][2];
			n_nexthard = tb_tasklevel[i+1][2];
		else
			break
		end
	end
	
	n_hardtime = floor(400 / (n_nexthard - n_hardbase));
	
	return (ring * 400 + (hard - n_hardbase) * n_hardtime + times + chain * 20);
end

