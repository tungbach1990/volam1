function Event1000_Gen_Boss(tbMapName)
	local  nX, nY = 0, 0
	local nMapId, nMapIndex, nNpcId, nPosX, nPosY, szNpcName, nParam =0, 0, 0, 0, 0, 0, 0
	local nIdxBoss = 0
	for i=1, getn(tbMapName) do
		for k = 1, getn(tbMapName[i]) do
			nMapId = tbMapName[i][k].nMapId
			nMapIndex = SubWorldID2Idx(tbMapName[i][k].nMapId)
			nNpcId = tbMapName[i][k].nNpcId
			nPosX = tbMapName[i][k].PosX
			nPosY = tbMapName[i][k].PosY
			szNpcName = tbMapName[i][k].szNpcName
			nParam = tbMapName[i][k].nParam
			for j = 1, tbMapName[i][k].nCount do
				local n5hanh = random(0, 4)
				nX = random(nPosX - 3, nPosX + 3)
				nY = random(nPosY - 4, nPosY + 4)
				local IndexNpc = AddNpcEx(nNpcId, 11, n5hanh, nMapIndex, nX * 32, nY * 32, 1,  szNpcName, 1)
				SetNpcParam(IndexNpc, 1, nParam)
				if ( nParam == 1000 ) then
					SetNpcScript(IndexNpc, "\\script\\EventThangLong1000\\box\\openbox.lua")
				end
				if ( nParam ~= 1001 and nParam ~= 1000 ) then
					SetNpcDeathScript(IndexNpc, "\\script\\EventThangLong1000\\boss\\npcdeath.lua");
					nIdxBoss = IndexNpc
				end
				
				if (nParam == 1001 and IndexNpc ~= 0) then
					if (nIdxBoss ~= 0) then
						if (tbNpcIndex[nIdxBoss] == nil) then
							tbNpcIndex[nIdxBoss] = {}
						end
							tbNpcIndex[nIdxBoss][getn(tbNpcIndex[nIdxBoss])+1] = IndexNpc
					end
				end
			end
		end
	end
end

